
var pary=document.getElementById('pary')

function btn_1() {
   if (pary.style.fontWeight=="800"){

        pary.style.fontWeight="400"
   }else{
        pary.style.fontWeight="800"

   }

}


function btn_2() {
   
    if (pary.style.fontStyle=="italic") {

        pary.style.fontStyle="normal"
    }else{
        pary.style.fontStyle="italic"
    }
}